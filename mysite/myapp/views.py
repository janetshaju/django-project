def expense_report(request):
    sort_by = request.GET.get('sort_by', 'category__name')  # Default sort by category name
    threshold = request.GET.get('threshold', None)

    # Filter and sort data
    report_data = Book.objects.values('category__name').annotate(total_expense=Sum('distribution_expense'))

    if threshold:
        report_data = report_data.filter(total_expense__gte=threshold)

    report_data = report_data.order_by(sort_by)
    return render(request, 'expense_report.html', {'report_data': report_data})
